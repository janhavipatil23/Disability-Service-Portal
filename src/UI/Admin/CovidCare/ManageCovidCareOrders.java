    public void viewOrderDirList() {
        ordersModel.getDataVector().removeAllElements();
        for (int i = 0; i < ccOrganization.getWorkQueue().getWorkRequestList().size(); i++) {
            CovidCareWorkRequest nurse = (CovidCareWorkRequest) ccOrganization.getWorkQueue().getWorkRequestList().get(i);
            System.out.println(nurse + " admin list ");
            if (!nurse.getStatus().equals("Sent")) {
                ordersModel.addRow(new Object[]{
                    ccOrganization.getWorkQueue().getWorkRequestList().get(i).getId(),
                    nurse.getAppointmentDate(),
                    ccOrganization.getWorkQueue().getWorkRequestList().get(i).getStatus()
                });
            }
        }
    }

