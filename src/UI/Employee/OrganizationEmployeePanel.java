    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        if (serviceTable.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Please select a row");
            return;
        }
        String b = String.valueOf(model.getValueAt(serviceTable.getSelectedRow(), 2));
        if (!b.equalsIgnoreCase("Accepted")) {
            JOptionPane.showMessageDialog(this, "Cant select a completed service");
            return;
        }
        selectedDir = organization.getWorkQueue().getWorkRequestList().get(serviceTable.getSelectedRow());
        if (statusCombo.getSelectedItem() != null && selectedDir != null) {
            String selectedItem = (String) statusCombo.getSelectedItem();
            selectedDir.setStatus(selectedItem);
            JOptionPane.showMessageDialog(this, "Status changed successfully");
        }
        viewOrderDirList();
  
    }//GEN-LAST:event_jButton1ActionPerformed

