    public void viewOrderDirList() {
        model.getDataVector().removeAllElements();
        for (int i = 0; i < organization.getWorkQueue().getWorkRequestList().size(); i++) {
            WorkRequest nurse = organization.getWorkQueue().getWorkRequestList().get(i);
            System.out.println(userAccount.getUsername() + " userAccount " + nurse.getReceiver());
            if (userAccount == nurse.getReceiver()) {
                model.addRow(new Object[]{
                    nurse.getSender().getUsername(),
                    nurse.getId(),
                    nurse.getStatus(),
                    nurse.getRequestDate(),
                    nurse.getSender().getUserDetails().getAddressApt()
                });
            }
        }
    }


